package org.stranger2015.opencv.fic.utils;

public
class Point {
    protected int x;
    protected int y;

    public
    Point ( int x, int y ) {
        this.x = x;
        this.y = y;
    }

    public
    Point () {
    }

    public
    int getX () {
        return x;
    }

    public
    int getY () {
        return y;
    }
}

